namespace GraduateWork.Client.Models.ResponseModels
{
    /// <summary>
    /// University response model.
    /// </summary>
    public class UniversityDto
    {
        /// <summary>
        /// Gets/Sets full name of university.
        /// </summary>
        public string FullName { get; set; }

        /// <summary>
        /// Gets/Sets level of accreditation of university.
        /// </summary>
        public string LevelOfAccreditation { get; set; }

        /// <summary>
        /// Gets/Sets university ownership.
        /// </summary>
        public string Ownership { get; set; }

        /// <summary>
        /// Gets/Sets university chief.
        /// </summary>
        public string Chief { get; set; }

        /// <summary>
        /// Gets/Sets university subordination.
        /// </summary>
        public string Subordination { get; set; }

        /// <summary>
        /// Gets/Sets university post index.
        /// </summary>
        public string PostIndex { get; set; }

        /// <summary>
        /// Gets/Sets university address.
        /// </summary>
        public string Address { get; set; }

        /// <summary>
        /// Gets/Sets university phone.
        /// </summary>
        public string Phone { get; set; }

        /// <summary>
        /// Gets/Sets university site.
        /// </summary>
        public string Site { get; set; }

        /// <summary>
        /// Gets/Sets university email.
        /// </summary>
        public string Email { get; set; }
    }
}
